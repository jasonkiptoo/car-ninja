package org.example.carassignment;

public class Car {
    private int id;
    private String make;
    private String model;
    private int year;
    private int cityMpg;
    private String carClass;
    private int combinationMpg;
    private int cylinders;
    private double displacement;
    private String drive;
    private String fuelType;
    private int highwayMpg;
    private String transmission;

    // Getters and setters
    public int getId() { return id; }
    public void setId(int id) { this.id = id; }

    public String getMake() { return make; }
    public void setMake(String make) { this.make = make; }

    public String getModel() { return model; }
    public void setModel(String model) { this.model = model; }

    public int getYear() { return year; }
    public void setYear(int year) { this.year = year; }

    public int getCityMpg() { return cityMpg; }
    public void setCityMpg(int cityMpg) { this.cityMpg = cityMpg; }

    public String getCarClass() { return carClass; }
    public void setCarClass(String carClass) { this.carClass = carClass; }

    public int getCombinationMpg() { return combinationMpg; }
    public void setCombinationMpg(int combinationMpg) { this.combinationMpg = combinationMpg; }

    public int getCylinders() { return cylinders; }
    public void setCylinders(int cylinders) { this.cylinders = cylinders; }

    public double getDisplacement() { return displacement; }
    public void setDisplacement(double displacement) { this.displacement = displacement; }

    public String getDrive() { return drive; }
    public void setDrive(String drive) { this.drive = drive; }

    public String getFuelType() { return fuelType; }
    public void setFuelType(String fuelType) { this.fuelType = fuelType; }

    public int getHighwayMpg() { return highwayMpg; }
    public void setHighwayMpg(int highwayMpg) { this.highwayMpg = highwayMpg; }

    public String getTransmission() { return transmission; }
    public void setTransmission(String transmission) { this.transmission = transmission; }
}
